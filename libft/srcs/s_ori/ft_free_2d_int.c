#include <stdlib.h>

void	ft_free_2d_int(int ***tab, int y)
{
	int c;

	c = 0;
	while (c < y)
	{
		free(tab[0][c]);
		c++;
	}
	free(tab[0]);
	tab[0] = 0;
}
